package org.example;

public class BucleWhile {
    public static void main(String[] args){

        int i = 0;
        // recibe un condicion para luego ejecutarse
        while (i< 10){
            System.out.println(i);
            i++;
        }
        int a = 0;

        // bucle do while recibe la ejecucion y luego verifica si se cumple la condicion
        do {

            System.out.println(a);
              a++;
        }while ( a < 10);



    }
}
